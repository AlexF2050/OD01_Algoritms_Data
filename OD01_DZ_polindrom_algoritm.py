### Алгоритм:
# 1. Определение палиндрома:
#    Строка является палиндромом, если она читается одинаково слева направо и справа налево
#    Например, "радар", "шалаш"
# 2. Преобразование строки:
#   - Привести строку к единому регистру (например, все символы в нижний регистр), чтобы сравнение было нечувствительным к регистру.
#   - Удалить из строки все пробелы и, при необходимости, другие знаки препинания, чтобы они не мешали проверке.
# 3. Сравнение строк:
#   - Сравнить исходную строку (после преобразования) с её перевернутой копией.
#   - Если они равны, строка является палиндромом.
#   - Если они не равны, строка не является палиндромом.

### Напишем функцию на Python, используя этот алгоритм:
def is_palindrome(s):
    s = s.lower().replace(" ", "")              # Преобразуем строку в нижний регистр и удаляем пробелы
    import re                                   # Удаляем знаки препинания (если это необходимо)
    s = re.sub(r'[^a-zа-я0-9]', '', s) # Удаляем все символы кроме букв и цифр
    return s == s[::-1]                         # Сравниваем строку с её перевернутой копией

# Пример использования
print("Доброго времени!")
print("Эта программа проверяет, является ли строка палиндромом.")
string = input("Введите строку, используя латинские и кириллические буквы (En or Ru): ")
if is_palindrome(string):
    print("Строка является палиндромом")
else:
    print("Строка не является палиндромом")